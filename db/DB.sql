CREATE TABLE IF NOT EXISTS PERSONA (
    ID INTEGER PRIMARY KEY AUTOINCREMENT,
    NOMBRE VARCHAR(100), 
    CONTRASEÑA INTEGER,
    COMPAÑIA VARCHAR(100)
);
CREATE TABLE IF NOT EXISTS PRODUCTOS(
    CODIGO INTEGER PRIMARY KEY UNIQUE, 
    NOMBRE VARCHAR,
    ACTIVO INTEGER,
    TIPO INTEGER, 
    CATEGORIA VARCHAR, 
    SUB_CATEGORIA VARCHAR,
    COMENTARIO VARCHAR,
    ADICIONAR INTEGER,
    FACTURAR_EXISTENCIA INTEGER,
    CANTIDAD_INICIAL INTEGER, 
    CANTIDAD_MINIMA INTEGER, 
    CODIGO_FABRICANTE INTEGER,
    BODEGA_ASIGNADO VARCHAR, 
    UB_FISICA VARCHAR, 
    VENDE_POR VARCHAR, 
    COMPRA_POR VARCHAR,
    CONTIENE INTEGER,
    FACTURAR_CON_PRECIO INTEGER,
    P1 INTEGER, 
    P2 INTEGER, 
    P3 INTEGER,
    P4 INTEGER, 
    PI1 INTEGER, 
    PI2 INTEGER, 
    PI3 INTEGER, 
    PI4 INTEGER,
    APLICAR_EN_VENTAS INTEGER, 
    APLICAR_EN_COMPRAS INTEGER, 
    NAME_IMAGE VARCHAR
);
